//Ação de On Click //

function clickCursoBasico() {

    document.getElementById("image-default").style.display = 'block';
    document.getElementById("image_explosivejkd").style.display = 'none';
    document.getElementById("image_atmospherejkd_combos").style.display = 'none';
};


function clickExoplosiveFull() {

    document.getElementById("image-default").style.display = 'none';
    document.getElementById("image_explosivejkd").style.display = 'block';
    document.getElementById("image_atmospherejkd_combos").style.display = 'none';
};

function clickJKDCombos() {

    document.getElementById("image-default").style.display = 'none';
    document.getElementById("image_explosivejkd").style.display = 'none';
    document.getElementById("image_atmospherejkd_combos").style.display = 'block';
};
